class Solution {
      void reorderlist(Node head) {
        if (head == null || head.next == null)
            return;
        Node dummy = new Node(0);
        dummy.next = head;
        Node runner = dummy;
        Node temp = head;
        Node mid = findMid(head);
        Node rev = reverseNode(mid.next);
        mid.next = null;

    
        while (temp != null || rev != null) {
            if (temp != null) {
                runner.next = temp;
                runner = runner.next;
                temp = temp.next;
            }
            if (rev != null) {
                runner.next = rev;
                runner = runner.next;
                rev = rev.next;
            }
        }

    }

    public Node reverseNode(Node node) {
        Node prev = null;
        Node curr = node;

        while (curr != null) {
            Node next = curr.next;
            curr.next = prev;
            prev = curr;
            curr = next;
        }
        return prev;
    }

    public Node findMid(Node head) {
        Node slow = head;
        Node fast = head;

        while (fast != null && fast.next != null) {
            fast = fast.next.next;
            slow = slow.next;
        }
        return slow;
    }
}
