class Solution
{   
    public:
    //Function to return list of integers visited in snake pattern in matrix.
    vector<int> snakePattern(vector<vector<int> > matrix)
    {   
        // code here
        vector<int> res ;
        
        int row = matrix.size();
        int col = matrix[0].size();
        
        for(int i = 0;i<row;i++){
            for(int j=0;j<col;j++){
                if(i%2==0){
                    res.push_back(matrix[i][j]);
                }else if(i%2!=0){
                    res.push_back(matrix[i][col-1-j]);
                }
            }
        }
        return res;
    }
};

